    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    String referencia;
    boolean encontrado = false;

    if (txtReferencia.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debes escribir una referencia", "Referencia", JOptionPane.WARNING_MESSAGE);
        txtReferencia.requestFocus();
    } else {
        referencia = txtReferencia.getText();

        for (int i = 0; i < inventarioDeRopa.size(); i++) {
            if (inventarioDeRopa.get(i).getReferencia().equalsIgnoreCase(referencia)) {
                appRopa.indiceAConsultar = i;
                appRopa.referenciaAConsultar = referencia;
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            referencia = null;
            JOptionPane.showMessageDialog(null, "La referencia " + referencia + " no se encuentra en la lista.");
            txtReferencia.requestFocus();
        } else {
            String nombre = inventarioDeRopa.get(appRopa.indiceAConsultar).getReferencia();
            double precio = inventarioDeRopa.get(appRopa.indiceAConsultar).getValorVenta();
            txtArticulo.setText(inventarioDeRopa.get(appRopa.indiceAConsultar).getArticulo());
            
            int cantidad = (int) spCantidad.getValue(); // Obtener la cantidad del JSpinner

             double total = precio * cantidad; // Calcular el total multiplicando el precio por la cantidad

             // Mostrar el total en el campo de texto txtPrecio
             txtPrecio.setText(String.valueOf(total));
           
        }
    }

    }//GEN-LAST:event_btnBuscarActionPerformed

    
